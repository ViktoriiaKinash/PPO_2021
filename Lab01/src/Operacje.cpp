#include <cmath>

float Kwadrat(const float FirstCoefficient)
{
  return sqrt(FirstCoefficient);
}

float Iloczyn(const float FirstCoefficient, const float SecondCoefficient)
{
  return FirstCoefficient*SecondCoefficient;
}

float Suma(const float FirstCoefficient, const float SecondCoefficient, const float ThirdCoefficient)
{
  return FirstCoefficient+SecondCoefficient+ThirdCoefficient;
}
